    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String nome = (String)cmbUsuarios.getSelectedItem();
        UsuarioDao uDao = new UsuarioDao();
        Usuario u = uDao.FindUserByName(nome);
        txtIdUsuario.setText(String.valueOf(u.getIdUsuario()));
        txtNomeUsuario.setText(String.valueOf(u.getNome()));
        txtSenhaUsuario.setText(String.valueOf(u.getSenha()));
        btnAlterar.setVisible(true);
        txtIdUsuario.setVisible(true);
        txtNomeUsuario.setVisible(true);
        txtSenhaUsuario.setVisible(true);
        lblId.setVisible(true);
        lblNome.setVisible(true);
        lblSenha.setVisible(true);
        txtIdUsuario.setEditable(false);
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int dado = JOptionPane.showConfirmDialog(rootPane, "Deseja modificar os dados??");
        switch(dado){
            
            case 0://sim
                UsuarioDao uDao = new UsuarioDao();
                Usuario u = new Usuario();
                u.setIdUsuario(Integer.parseInt(txtIdUsuario.getText()));
                u.setNome(String.valueOf(txtNomeUsuario.getText()));
                u.setSenha(String.valueOf(txtSenhaUsuario.getText()));
                uDao.Update(u);
                
                if(uDao.Update(u) == true)
                JOptionPane.showMessageDialog(null, "Usuário Atualizado com Sucesso!!");
                txtIdUsuario.setText("");
                txtNomeUsuario.setText("");
                txtSenhaUsuario.setText("");
                cmbUsuarios.removeAllItems();
                ComboUsuarios();
                
            break;
            
            case 1://não
                JOptionPane.showMessageDialog(null, "Usuário não foi modificado!!");
            break;
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

