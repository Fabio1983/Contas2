    public void ComboUsuarios(){
    
     UsuarioDao uDao = new UsuarioDao();
     list = uDao.getUsuario();
    if(list.isEmpty()){
     this.dispose();
    }
    else
    {
      for (int i = 0; i < list.size(); i++)
      {
        cmbUsuarios.addItem(list.get(i).getNome());
      }
    }    
  }
